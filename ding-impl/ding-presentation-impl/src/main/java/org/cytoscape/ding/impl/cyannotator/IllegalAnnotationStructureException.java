package org.cytoscape.ding.impl.cyannotator;

@SuppressWarnings("serial")
public class IllegalAnnotationStructureException extends IllegalArgumentException {

	public IllegalAnnotationStructureException() {
		super();
	}

	public IllegalAnnotationStructureException(String message, Throwable cause) {
		super(message, cause);
	}

	public IllegalAnnotationStructureException(String s) {
		super(s);
	}

	public IllegalAnnotationStructureException(Throwable cause) {
		super(cause);
	}

}
